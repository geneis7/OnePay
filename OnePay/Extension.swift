// CGFloat 값을 간단하게 헥사 코드로 입력 할 수 있는 코드
// -- used --
// let textFieldBackGroundColor = UIColor(hexString: "#EBEBEBFF")
// ----------
extension UIColor {
    public convenience init?(hexString: String) {
        let r, g, b, a: CGFloat
        
        if hexString.hasPrefix("#") {
            let start = hexString.index(hexString.startIndex, offsetBy: 1)
            let hexColor = String(hexString[start...])
            
            if hexColor.count == 8 {
                let scanner = Scanner(string: hexColor)
                var hexNumber: UInt64 = 0
                
                if scanner.scanHexInt64(&hexNumber) {
                    r = CGFloat((hexNumber & 0xff000000) >> 24) / 255
                    g = CGFloat((hexNumber & 0x00ff0000) >> 16) / 255
                    b = CGFloat((hexNumber & 0x0000ff00) >> 8) / 255
                    a = CGFloat(hexNumber & 0x000000ff) / 255
                    
                    self.init(red: r, green: g, blue: b, alpha: a)
                    return
                }
            }
        }
        return nil
    }
}

// 숫자 3자리마다 콤마를 넣거나 빼는 코드
// -- used --
// let a: String = (123456789).withComma
// let b: String = a.delComma
// let c: String = "123,45a".delComma
// ----------
extension Int {
    var withComma: String {
        let decimalFormatter = NumberFormatter()
        decimalFormatter.numberStyle = NumberFormatter.Style.decimal
        decimalFormatter.groupingSeparator = ","
        decimalFormatter.groupingSize = 3
        
        return decimalFormatter.string(from: self as NSNumber)!
    }
}

extension String {
    var delComma: String {
        if self.utf8CString.count > 0 {
            return self.replacingOccurrences(of: ",", with: "")
        } else {
            return "0"
        }

    }
}

